package com.playstarnet.essentials.feat.ui;

public class Sticker {
    private String name;        // Name of the sticker
    private String displayChar; // Display character for the sticker
    private int width;          // Width of the sticker
    private int height;         // Height of the sticker

    // Getters and setters for each field
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDisplayChar() {
        return displayChar;
    }

    public void setDisplayChar(String displayChar) {
        this.displayChar = displayChar;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
